def apply_validated_style(row):
    try:
        start_h = int(row['시간'].split(':')[0])
        is_now = (start_h == now.hour)
    except:
        is_now = False
    
    styles = []
    for val in row:
        base = 'color: black; font-weight: bold; '
        if is_now: base += 'background-color: #FFF9C4; border: 2.5px solid orange; '
        v = str(val)
        if '로비' in v: base += 'color: #D32F2F;'
